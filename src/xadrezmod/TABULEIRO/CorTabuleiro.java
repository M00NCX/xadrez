package xadrezmod.TABULEIRO;


public class CorTabuleiro {
    private int corTab;
    private boolean isOcupado;

    /**
     * @return the corTab
     */
    public int getCorTab() {
        return corTab;
    }

    /**
     * @param corTab the corTab to set
     */
    public void setCorTab(int corTab) {
        this.corTab = corTab;
    }

    /**
     * @return the isOcupado
     */
    public boolean isIsOcupado() {
        return isOcupado;
    }

    /**
     * @param isOcupado the isOcupado to set
     */
    public void setIsOcupado(boolean isOcupado) {
        this.isOcupado = isOcupado;
    }
}
